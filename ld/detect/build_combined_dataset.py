"""Build a YOLO dataset from data/combined* JSON annotations.

Each sample folder contains:
  <id>_frame.png      -- cursor-stripped frame
  <id>_annotation.png -- visual reference (not used for training)
  <id>.json           -- marks[].bbox = [x, y, w, h] pixel boxes (class: shape)

Val split defaults to n* clips (combined_n hold-out); everything else trains.

Usage:
    python -m ld.detect.build_combined_dataset
    python -m ld.detect.build_combined_dataset --val-prefix n
"""
from __future__ import annotations

import argparse
import json
import shutil
from pathlib import Path

from ld.config import DATA_DIR, DETECT_CLASS_NAMES
from ld.detect.boxes import Box, to_yolo_line

__all__ = ["build_combined_dataset"]

COMBINED_GLOBS = ("combined", "combined 2", "combined 3")
OUT_DIR = DATA_DIR / "detect" / "dataset_combined"


def _combined_dirs(root: Path = DATA_DIR) -> list[Path]:
    return [root / name for name in COMBINED_GLOBS if (root / name).is_dir()]


def _clip_stem(json_path: Path) -> str:
    meta = json.loads(json_path.read_text())
    return str(meta.get("clip") or json_path.stem)


def _yolo_lines(meta: dict, img_w: int, img_h: int) -> list[str]:
    lines: list[str] = []
    for mark in meta.get("marks", []):
        x, y, w, h = mark["bbox"]
        x1 = max(0.0, float(x))
        y1 = max(0.0, float(y))
        x2 = min(float(img_w), float(x) + float(w))
        y2 = min(float(img_h), float(y) + float(h))
        box = Box(0, x1, y1, x2, y2)
        if box.valid():
            lines.append(to_yolo_line(box, img_w, img_h))
    return lines


def build_combined_dataset(*, out_dir: Path = OUT_DIR,
                           val_prefix: str = "n") -> Path:
    samples: list[tuple[str, Path, Path]] = []
    for folder in _combined_dirs():
        for jp in sorted(folder.glob("*.json")):
            stem = _clip_stem(jp)
            frame = folder / f"{stem}_frame.png"
            if not frame.exists():
                # fallback: json named s30.json -> s30_frame.png
                frame = folder / f"{jp.stem}_frame.png"
            if not frame.exists():
                print(f"warning: no frame for {jp}")
                continue
            samples.append((stem, frame, jp))

    if not samples:
        raise SystemExit("No combined JSON samples found under data/combined*")

    val_ids = {stem for stem, _, _ in samples if stem.startswith(val_prefix)}
    print(f"samples={len(samples)}  train={len(samples) - len(val_ids)}  "
          f"val={len(val_ids)} ({sorted(val_ids)})")

    out_dir = Path(out_dir)
    for sub in ("images/train", "images/val", "labels/train", "labels/val"):
        d = out_dir / sub
        if d.exists():
            shutil.rmtree(d)
        d.mkdir(parents=True, exist_ok=True)

    import cv2
    n_train = n_val = 0
    for stem, frame_path, json_path in samples:
        split = "val" if stem in val_ids else "train"
        img = cv2.imread(str(frame_path))
        if img is None:
            print(f"warning: unreadable {frame_path}")
            continue
        h, w = img.shape[:2]
        meta = json.loads(json_path.read_text())
        lines = _yolo_lines(meta, w, h)
        if not lines:
            print(f"warning: no valid boxes in {json_path}")
            continue

        out_name = f"{stem}.png"
        shutil.copy2(frame_path, out_dir / f"images/{split}" / out_name)
        (out_dir / f"labels/{split}" / f"{stem}.txt").write_text("\n".join(lines) + "\n")
        if split == "val":
            n_val += 1
        else:
            n_train += 1

    yaml_path = out_dir / "dataset.yaml"
    names = "\n".join(f"  {i}: {n}" for i, n in enumerate(DETECT_CLASS_NAMES))
    yaml_path.write_text(
        f"# Auto-generated by ld.detect.build_combined_dataset\n"
        f"path: {out_dir.resolve()}\n"
        f"train: images/train\n"
        f"val: images/val\n"
        f"names:\n{names}\n"
    )
    print(f"train={n_train} val={n_val} -> {out_dir}\nyaml -> {yaml_path}")
    return yaml_path


def main() -> None:
    ap = argparse.ArgumentParser(description="Build YOLO dataset from data/combined*")
    ap.add_argument("--out-dir", default=str(OUT_DIR))
    ap.add_argument("--val-prefix", default="n",
                    help="clip stems starting with this go to val (default: n1,n2)")
    args = ap.parse_args()
    build_combined_dataset(out_dir=Path(args.out_dir), val_prefix=args.val_prefix)


if __name__ == "__main__":
    main()
