"""Build a YOLO dataset from data/detect/s_frames + s_labels.

Reads the cursor-stripped PNGs from s_frames/ and the YOLO .txt labels
from s_labels/, applies a clip-wise train/val split (hold out one clip
per N for validation), and writes the standard YOLO directory tree.

Val split: every Nth clip by stem (default N=5, so ~20% held out).
Clips are sorted alphabetically: s1,s10,s11,s12,s2,...,s9.

Usage:
    python -m ld.detect.build_s_dataset
    python -m ld.detect.build_s_dataset --val-every 4 --out-dir data/detect/dataset_s2
"""
from __future__ import annotations

import argparse
import shutil
from pathlib import Path

from ld.config import DATA_DIR

# Two-class scheme: full shapes vs border-truncated partial shapes
S_CLASS_NAMES = ["shape_full", "shape_partial"]

S_FRAMES_DIR = DATA_DIR / "detect" / "s_frames"
S_LABELS_DIR = DATA_DIR / "detect" / "s_labels"
OUT_DIR       = DATA_DIR / "detect" / "dataset_s"


def build_s_dataset(
    frames_dir: Path = S_FRAMES_DIR,
    labels_dir: Path = S_LABELS_DIR,
    out_dir: Path = OUT_DIR,
    val_every: int = 5,
) -> Path:
    frames_dir = Path(frames_dir)
    labels_dir = Path(labels_dir)
    out_dir = Path(out_dir)

    # Collect frames that have a matching label file
    samples: list[tuple[str, Path, Path]] = []
    for png in sorted(frames_dir.glob("*.png")):
        lbl = labels_dir / f"{png.stem}.txt"
        if not lbl.exists():
            print(f"warning: no label for {png.name}, skipping")
            continue
        if not lbl.read_text().strip():
            print(f"warning: empty label for {png.name}, skipping")
            continue
        samples.append((png.stem, png, lbl))

    if not samples:
        raise SystemExit(f"No labelled frames found in {frames_dir}")

    # Clip-wise split: group by clip stem (e.g. "s1"), assign every Nth clip to val
    from collections import defaultdict
    by_clip: dict[str, list] = defaultdict(list)
    for stem, png, lbl in samples:
        clip = stem.rsplit("_", 1)[0]  # "s1_00115" -> "s1"
        by_clip[clip].append((stem, png, lbl))

    clips = sorted(by_clip.keys(), key=lambda c: (c[0], int(c[1:]) if c[1:].isdigit() else 0))
    val_clips = {c for i, c in enumerate(clips) if (i + 1) % val_every == 0}
    train_clips = set(clips) - val_clips
    print(f"clips={len(clips)}  train_clips={sorted(train_clips)}  val_clips={sorted(val_clips)}")

    # Build output tree
    for sub in ("images/train", "images/val", "labels/train", "labels/val"):
        d = out_dir / sub
        if d.exists():
            shutil.rmtree(d)
        d.mkdir(parents=True, exist_ok=True)

    n_train = n_val = 0
    for clip, entries in by_clip.items():
        split = "val" if clip in val_clips else "train"
        for stem, png, lbl in entries:
            out_name = f"{stem}.png"
            shutil.copy2(png, out_dir / f"images/{split}" / out_name)
            shutil.copy2(lbl, out_dir / f"labels/{split}" / f"{stem}.txt")
            if split == "val":
                n_val += 1
            else:
                n_train += 1

    yaml_path = out_dir / "dataset.yaml"
    names = "\n".join(f"  {i}: {n}" for i, n in enumerate(S_CLASS_NAMES))
    yaml_path.write_text(
        f"# Auto-generated by ld.detect.build_s_dataset\n"
        f"path: {out_dir.resolve()}\n"
        f"train: images/train\n"
        f"val: images/val\n"
        f"names:\n{names}\n"
    )
    print(f"train={n_train} val={n_val} -> {out_dir}\nyaml -> {yaml_path}")
    return yaml_path


def main() -> None:
    ap = argparse.ArgumentParser(description="Build YOLO dataset from s_frames/s_labels")
    ap.add_argument("--frames-dir", default=str(S_FRAMES_DIR))
    ap.add_argument("--labels-dir", default=str(S_LABELS_DIR))
    ap.add_argument("--out-dir", default=str(OUT_DIR))
    ap.add_argument("--val-every", type=int, default=5,
                    help="hold out every Nth clip for val (default 5 = ~20%%)")
    args = ap.parse_args()
    build_s_dataset(
        Path(args.frames_dir), Path(args.labels_dir),
        Path(args.out_dir), args.val_every,
    )


if __name__ == "__main__":
    main()
